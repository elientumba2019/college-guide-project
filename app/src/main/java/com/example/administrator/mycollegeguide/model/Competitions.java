public class Competitions {

    private String competitionId;
    private String competitionName;
    private String competitionRequirement;


    public String getCompetitionId() {
        return competitionId;
    }

    public void setCompetitionId(String competitionId) {
        this.competitionId = competitionId;
    }

    public String getCompetitionName() {
        return competitionName;
    }

    public void setCompetitionName(String competitionName) {
        this.competitionName = competitionName;
    }

    public String getCompetitionRequirement() {
        return competitionRequirement;
    }

    public void setCompetitionRequirement(String competitionRequirement) {
        this.competitionRequirement = competitionRequirement;
    }
}
